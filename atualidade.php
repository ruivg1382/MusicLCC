<p class="text-justify animate__animated animate__fadeInLeft"><strong class="text-warning bg-dark">CvMusic:</strong> <span class=""><strong>Qual foi ou é a vossa maior dificuldade na música?</strong></span><br>
<strong class="text-primary">Rudy:</strong> A nossa maior dificuldade é a de encontrar um Studio acessível para gravarmos o nosso trabalho, também temos dificuldades na produção de vídeo, o que podia
de uma certa forma promover o grupo e torna-lo mais popular.</p>

<p class="text-justify animate__animated animate__fadeInLeft"><strong class="text-warning bg-dark">CvMusic:</strong> <span class=""><strong>Qual é vosso género predileto e a partir de quando começaram trilhar nesse mundo da música?</strong></span><br>
<strong class="text-primary">Rudy:</strong> Nós começamos a cantar em 2016. Os generos da música favorita do grupo Krioulo Revolucionário são Hip Hop e Regae Music.</p>

<p class="text-justify animate__animated animate__fadeInLeft"><strong class="text-warning bg-dark">CvMusic:</strong><span class=""><strong>Qual o seu recado para os novos talentos que também sonham com sucesso na música?</strong></span><br>
